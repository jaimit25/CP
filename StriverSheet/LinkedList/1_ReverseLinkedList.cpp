class ListNode
{
public:
    int val;
    ListNode *next;

} * hd;

ListNode *reverseList(ListNode *head)
{
    ListNode *left = NULL;
    ListNode *middle = head;
    ListNode *right;

    while (middle != NULL)
    {
        right = middle->next;
        middle->next = left;
        left = middle;
        middle = right;
        }
    return left;
}

void printList(ListNode *h)
{
    while (h != NULL)
    {
        cout << h->val << " ";
        h = h->next;
    }
    cout << endl;
}

